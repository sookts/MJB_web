						<li>What is family law mediation?</li>
							<p>Mediation is a process in which parties involved in a dispute, such as divorce, try to negotiate a favorable divorce agreement with the guidance and support of a neutral third party mediator. The mediator helps all involved parties communicate and negotiate through the details so they can reach their own agreements in a way that is most beneficial and amicable.</p>

						<li>Why is mediation a better alternative than going to court?</li>
							<p>Fighting only makes these already difficult matters more challenging and potentially harmful to you and your family. Additionally, the courts are regularly very busy and backed up.  They really do want you to resolve as much of your own issues as possible.  Going through the courts can result in many delays.  Working against each other through litigation can take a very long time and drag things out painfully.  Mediation can save significant time, money and stress.</p>

						<li>How does the mediation process work?</li>
							<p>The truth is, all cases are different.  However, once we’ve discussed your circumstances, we’ll be able to provide you with a better idea of what you might expect.  The basic process involves meeting together with both parties to work through the details you’ll eventually need to agree upon.  Our role will be to guide you through the legal complexities, facilitate discussions as necessary, keep things focused on productive, mutually beneficial solutions and help you complete the necessary forms and processes.  Along the way, we’ll of course also be available to address any questions and resolve challenges if and when they arise.</p>

						<li>What costs can we expect if we choose mediation?</li>
							<p>Every case really is different.  However, where average court litigation costs for a divorce dispute may average between $15-$30,000, the average cost of using mediation to resolve similar circumstances ranges between about $4,000-$6,000.  That amount is typically divided between the involved parties.  So mediation is generally the less expensive of alternatives when there are legal disputes to be resolved.</p>

